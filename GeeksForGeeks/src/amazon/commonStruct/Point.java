package amazon.commonStruct;
/***
 * A class to represent a point in 2D space
 * @author kjivi
 *
 */
public class Point {
	private int x;
	private int y;
	
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}
	
	
}
